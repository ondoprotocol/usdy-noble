package cli

import (
	"context"
	"fmt"

	"github.com/cosmos/cosmos-sdk/client"
	"github.com/cosmos/cosmos-sdk/client/flags"
	"github.com/ondoprotocol/aura/x/aura/types"
	"github.com/spf13/cobra"
)

func GetQueryCmd() *cobra.Command {
	cmd := &cobra.Command{
		Use:                        types.ModuleName,
		Short:                      fmt.Sprintf("Querying commands for the %s module", types.ModuleName),
		DisableFlagParsing:         false,
		SuggestionsMinimumDistance: 2,
		RunE:                       client.ValidateCmd,
	}

	cmd.AddCommand(GetBlocklistQueryCmd())

	cmd.AddCommand(QueryDenom())
	cmd.AddCommand(QueryPaused())
	cmd.AddCommand(QueryOwner())
	cmd.AddCommand(QueryBurners())
	cmd.AddCommand(QueryMinters())
	cmd.AddCommand(QueryPausers())

	return cmd
}

func QueryDenom() *cobra.Command {
	cmd := &cobra.Command{
		Use:   "denom",
		Short: "Query the module's denom",
		Args:  cobra.NoArgs,
		RunE: func(cmd *cobra.Command, args []string) error {
			clientCtx := client.GetClientContextFromCmd(cmd)
			queryClient := types.NewQueryClient(clientCtx)

			res, err := queryClient.Denom(context.Background(), &types.QueryDenom{})
			if err != nil {
				return err
			}

			return clientCtx.PrintProto(res)
		},
	}

	flags.AddQueryFlagsToCmd(cmd)

	return cmd
}

